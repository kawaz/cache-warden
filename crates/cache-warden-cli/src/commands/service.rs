    use super::*;

    fn sample_def() -> ServiceDefinition {
        ServiceDefinition::for_daemon(
            "/opt/cache-warden/bin/cache-warden",
            "com.github.kawaz.cache-warden",
            Some("/tmp/cw.sock"),
            Some("/home/k/.config/cache-warden/config.toml"),
            Some("/home/k/Library/Logs/cache-warden/daemon.log".to_string()),
        )
    }

    // ---- ServiceDefinition::for_daemon ----

    #[test]
    fn for_daemon_builds_program_args_with_socket() {
        let d = sample_def();
        assert_eq!(
            d.program_args,
            vec![
                "/opt/cache-warden/bin/cache-warden",
                "daemon",
                "run",
                "--socket",
                "/tmp/cw.sock"
            ]
        );
    }

    #[test]
    fn for_daemon_without_socket_omits_socket_args() {
        let d = ServiceDefinition::for_daemon("/bin/cw", "L", None, None, None);
        assert_eq!(d.program_args, vec!["/bin/cw", "daemon", "run"]);
        // No config → only PATH is baked in.
        assert_eq!(d.env.keys().collect::<Vec<_>>(), vec!["PATH"]);
    }

    #[test]
    fn for_daemon_bakes_config_when_present() {
        let d = sample_def();
        assert_eq!(
            d.env.get("CACHE_WARDEN_CONFIG").map(String::as_str),
            Some("/home/k/.config/cache-warden/config.toml")
        );
        assert!(d.env.contains_key("PATH"));
    }

    // ---- launchd plist golden ----

    #[test]
